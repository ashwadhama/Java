//Write a java program to display the following pattern
class p10
{
    public static void main(String args[])
    {
        for(int i=1;i<=5;i++)
        {
            for(int j=i;j<=5;j++)
            {
                
                System.out.print("* ");            
            }    
            System.out.println(" ");    
            
        }    
    }
}
